          <i class="fas fa-chevron-down" :class="{ 'rotate': showDateDropdown }"></i>
        </button>
        
        <div v-show="showDateDropdown" class="dropdown-menu date-menu">
          <button 
            v-for="(label, range) in dateRanges" 
            :key="range"
            class="dropdown-item"
            :class="{ 'active': selectedRange === label }"
            @click="selectDateRange(range)"
          >
            {{ label }}
          </button>
          
          <div v-if="showCustomRange" class="custom-range">
            <div class="custom-range-header">Select Date Range</div>
            <div class="range-inputs">
              <div class="input-group">
                <label>Start Date</label>
                <input type="date" v-model="dateRange.start" :max="dateRange.end">
              </div>
              <div class="input-group">
                <label>End Date</label>
                <input type="date" v-model="dateRange.end" :min="dateRange.start">
              </div>
            </div>
