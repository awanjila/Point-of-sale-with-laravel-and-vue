<script>
import jsPDF from 'jspdf';
import 'jspdf-autotable';
import * as XLSX from 'xlsx';

export default {
  data() {
    return {
      businessName: localStorage.getItem('businessName') || '',
      currency: localStorage.getItem('currency') || '',
      userName: window.Laravel?.user?.name || 'Unknown User',
      currentDateTime: new Date().toLocaleString(),
      purchaseData: [],
      loading: false,
      showDateDropdown: false,
      showExportDropdown: false,
      showCustomRange: false,
      selectedRange: 'Today',
      dateRange: {
        start: new Date().toISOString().split('T')[0],
        end: new Date().toISOString().split('T')[0]
      },
      dateRanges: {
        today: 'Today',
        yesterday: 'Yesterday',
        thisWeek: 'This Week',
        thisMonth: 'This Month',
        thisYear: 'This Year',
        custom: 'Custom Range'
      }
    }
  },

  computed: {
    totalPurchases() {
      return this.purchaseData.reduce((sum, row) => sum + row.purchase_count, 0);
    },
    totalProducts() {
      return this.purchaseData.reduce((sum, row) => sum + row.products_purchased, 0);
    },
    totalAmount() {
      return this.purchaseData.reduce((sum, row) => sum + row.total_amount, 0);
    },
    totalDue() {
      return this.purchaseData.reduce((sum, row) => sum + row.due_amount, 0);
    }
  },

  methods: {
    formatCurrency(amount) {
      try {
        return new Intl.NumberFormat('en-KE', {
          style: 'currency',
          currency: this.currency || 'KES'
        }).format(amount);
      } catch (error) {
        return `${this.currency} ${amount}`;
      }
    },

    formatDate(date) {
      try {
        return new Date(date).toLocaleDateString();
      } catch (error) {
        return date;
      }
    },

    toggleDateDropdown() {
      this.showDateDropdown = !this.showDateDropdown;
      if (this.showExportDropdown) {
        this.showExportDropdown = false;
      }
    },

    toggleExportDropdown() {
      this.showExportDropdown = !this.showExportDropdown;
      if (this.showDateDropdown) {
        this.showDateDropdown = false;
      }
    },

    selectDateRange(range) {
      const today = new Date();
      let start = new Date();
      let end = new Date();

      switch (range) {
        case 'today':
          this.selectedRange = 'Today';
          break;

        case 'yesterday':
          start.setDate(today.getDate() - 1);
          end = new Date(start);
          this.selectedRange = 'Yesterday';
          break;

        case 'thisWeek':
          start.setDate(today.getDate() - today.getDay());
          this.selectedRange = 'This Week';
          break;

        case 'thisMonth':
          start = new Date(today.getFullYear(), today.getMonth(), 1);
          end = new Date(today.getFullYear(), today.getMonth() + 1, 0);
          this.selectedRange = 'This Month';
          break;

        case 'thisYear':
          start = new Date(today.getFullYear(), 0, 1);
          end = new Date(today.getFullYear(), 11, 31);
          this.selectedRange = 'This Year';
          break;

        case 'custom':
          this.showCustomRange = true;
          this.selectedRange = 'Custom Range';
          return;
      }

      this.dateRange = {
        start: start.toISOString().split('T')[0],
        end: end.toISOString().split('T')[0]
      };
      
      this.showDateDropdown = false;
      this.showCustomRange = false;
      this.fetchPurchaseData();
    },

    applyCustomRange() {
      this.showDateDropdown = false;
      this.showCustomRange = false;
      this.fetchPurchaseData();
    },

    handleClickOutside(event) {
      const dateDropdown = this.$refs.dateDropdown;
      const exportDropdown = this.$refs.exportDropdown;

      if (dateDropdown && !dateDropdown.contains(event.target)) {
        this.showDateDropdown = false;
      }
      if (exportDropdown && !exportDropdown.contains(event.target)) {
        this.showExportDropdown = false;
      }
    },

    async handleExportToPDF() {
      try {
        const doc = new jsPDF();
        const pageWidth = doc.internal.pageSize.width;
        
        // Add business header
        doc.setFontSize(16);
        const businessText = this.businessName || 'Purchase Report';
        const textWidth = doc.getTextWidth(businessText);
        const xPosition = (pageWidth - textWidth) / 2;
        doc.text(businessText, xPosition, 20);
        
        // Add metadata
        doc.setFontSize(10);
        doc.text(`Generated by: ${this.userName}`, 15, 30);
        doc.text(`Date: ${new Date().toLocaleString()}`, 15, 35);
        
        // Add summary section
        doc.setFontSize(12);
        doc.text('Summary', 15, 45);
        
        const summaryData = [
          ['Total Purchases', this.totalPurchases.toString()],
          ['Total Products', this.totalProducts.toString()],
          ['Total Amount', this.formatCurrency(this.totalAmount)],
          ['Total Due', this.formatCurrency(this.totalDue)]
        ];

        doc.autoTable({
          startY: 50,
          head: [['Metric', 'Value']],
          body: summaryData,
          theme: 'grid',
          styles: { fontSize: 10 },
          headStyles: { fillColor: [33, 150, 243] }
        });

        // Add main data table
        const tableData = this.purchaseData.map(row => [
          this.formatDate(row.purchase_date),
          row.purchase_count.toString(),
          row.products_purchased.toString(),
          this.formatCurrency(row.total_amount),
          this.formatCurrency(row.paid_amount),
          this.formatCurrency(row.due_amount)
        ]);

        doc.autoTable({
          startY: doc.previousAutoTable.finalY + 15,
          head: [['Date', 'Purchases', 'Products', 'Total', 'Paid', 'Due']],
          body: tableData,
          theme: 'grid',
          styles: { fontSize: 8 },
          headStyles: { fillColor: [33, 150, 243] }
        });

        const fileName = `${this.businessName.replace(/[^a-z0-9]/gi, '_').toLowerCase()}_purchase_report_${new Date().toISOString().split('T')[0]}.pdf`;
        doc.save(fileName);
        this.showExportDropdown = false;
      } catch (error) {
        console.error('PDF export failed:', error);
        alert('Failed to export PDF. Please try again.');
      }
    },

    async handleExportToExcel() {
      try {
        const wb = XLSX.utils.book_new();
        
        // Add summary worksheet
        const summaryData = [
          ['Business Name:', this.businessName],
          ['Generated By:', this.userName],
          ['Date:', new Date().toLocaleString()],
          [''],
          ['Summary'],
          ['Total Purchases:', this.totalPurchases],
          ['Total Products:', this.totalProducts],
          ['Total Amount:', this.formatCurrency(this.totalAmount)],
          ['Total Due:', this.formatCurrency(this.totalDue)]
        ];
        
        const wsSummary = XLSX.utils.aoa_to_sheet(summaryData);
        XLSX.utils.book_append_sheet(wb, wsSummary, "Summary");

        // Add details worksheet
        const wsData = this.purchaseData.map(row => ({
          'Date': this.formatDate(row.purchase_date),
          'Purchases': row.purchase_count,
          'Products': row.products_purchased,
          'Total Amount': this.formatCurrency(row.total_amount),
          'Paid Amount': this.formatCurrency(row.paid_amount),
          'Due Amount': this.formatCurrency(row.due_amount)
        }));
        
        const wsDetails = XLSX.utils.json_to_sheet(wsData);
        XLSX.utils.book_append_sheet(wb, wsDetails, "Details");

        const fileName = `${this.businessName.replace(/[^a-z0-9]/gi, '_').toLowerCase()}_purchase_report_${new Date().toISOString().split('T')[0]}.xlsx`;
        XLSX.writeFile(wb, fileName);
        
        this.showExportDropdown = false;
      } catch (error) {
        console.error('Excel export failed:', error);
        alert('Failed to export Excel file. Please try again.');
      }
    },

    async fetchPurchaseData() {
      try {
        this.loading = true;
        const response = await axios.get('/api/purchase-report-api', {
          params: {
            start_date: this.dateRange.start,
            end_date: this.dateRange.end
          }
        });

        if (Array.isArray(response.data)) {
          this.purchaseData = response.data;
        } else {
          console.error('Unexpected response format:', response.data);
          this.purchaseData = [];
        }
      } catch (error) {
        console.error('Error fetching purchase data:', error);
        this.purchaseData = [];
      } finally {
        this.loading = false;
      }
    }
  },

  mounted() {
    // Get user info from window.Laravel
    if (window.Laravel && window.Laravel.user) {
      this.userName = window.Laravel.user.name;
    }
    
    document.addEventListener('click', this.handleClickOutside);
    this.fetchPurchaseData();
  },

  beforeUnmount() {
    document.removeEventListener('click', this.handleClickOutside);
  }
}
</script>


