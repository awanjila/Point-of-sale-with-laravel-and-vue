          <span>Export</span>
          <i class="fas fa-chevron-down" :class="{ 'rotate': showExportDropdown }"></i>
        </button>
        
        <div v-show="showExportDropdown" class="dropdown-menu export-menu">
          <button type="button" class="dropdown-item" @click="handleExportToPDF">
            <i class="fas fa-file-pdf"></i>
            <span>Export to PDF</span>
          </button>
          <button type="button" class="dropdown-item" @click="handleExportToExcel">
            <i class="fas fa-file-excel"></i>
            <span>Export to Excel</span>
          </button>
        </div>
      </div>
